#include <stdio.h>
int main()
{
  /* Varible declaration to hold the input from the keyboard */
  int age;
  /* Ask the user for input */
  printf("How old are you? ");
  /* Read the number that comes from the keyboad input */
  scanf("%d", &age);
  /* Print the users input on the screen */
  printf("You are %d years old. \n", age);
  return 0;
}


